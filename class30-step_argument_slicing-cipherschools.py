# slicing / selecting sub sequences
lang = "Python"
# syntax - [start argument : stop argument -1: step ]
print("Trumil"[2:5])
print("Trumil"[0:5])
print("Trumil"[0:5:1])
print("Trumil"[0:5:2])
print("Trumil"[0: :3])
print("Trumil"[ : :-1]) #trick
print("Trumil"[5::-1])
print("Trumil"[-1: :-1])

